mood = input('What mood are you in now?: ')

if mood == 'happy':
    print('It is great to see you happy!')
elif mood == 'nervous':
    print('Take a deep breath 3 times.')
elif mood == 'sad':
    print('Don\'t be sad!')
elif mood == 'excited':
    print('I am excited too')
elif mood == 'relaxed':
    print('Wonderful')
else:
    print('I don\'t recognize this mood')